<h3>公司简介</h3>
<p>　</p>
<p>
上海力奇空调净化设备有限公司 是一家外商独资、净化设备 专业制造厂 。公司总经理魏金隆先生，凭其40多年的洁净行业之丰富经验及良好声誉，领导公司积极引进国外的先进技术和设备，凭借专精之工作团队，诚信之理念，至诚之服务，高品质之产品，以满足市场的不同需求。
</p>
<p>
上海力奇空调净化设备有限公司在上海江桥工业区拥有 7000 ㎡ 的厂房，并于 2003 年在苏州吴江汾湖工业区成立苏州力技净化设备有限公司 ， 注册资本额 230 万美元，建设 27000 ㎡ 的系统净化设备专业生产基地。
</p>
<p>
随着公司的不断发展，力奇已挤身于净化设备生产行业先进之列， 2002 年开始进入国际市场，服务于台湾、日本、韩国、新加坡、印度、东南亚、中东以及欧美等国内外洁净行业知名公司。
</p>
<p>
业务范围：<br />
专业制造高品质的净化设备 专业承接无尘无菌室工程<br />
专业生产系列净化彩钢板、净化门 承接各类洁净不锈钢制品<br />
